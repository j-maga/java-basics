package string;

public class CharAt {
	public static void main(String[] args) {
		String str = "Java String API";
		System.out.println("Char at index 0 - "+ str.charAt(0));
		System.out.println("Char at index 4 - "+ str.charAt(4));
		System.out.println("Char at index 10 - "+ str.charAt(10));
		System.out.println("Char at index 20"+ str.charAt(20));
	}
}
